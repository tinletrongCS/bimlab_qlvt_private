-- ---------------------------------------------------------------------------
-- Catalog items
-- ---------------------------------------------------------------------------

CREATE TABLE asset.asset_catalog_items (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_code VARCHAR(80) NOT NULL,
    name VARCHAR(255) NOT NULL,
    category_id BIGINT NULL,
    catalog_type VARCHAR(40) NOT NULL,
    inventory_group VARCHAR(120) NULL,
    unit VARCHAR(40) NULL,
    cost_value NUMERIC(18,2) NULL,
    standard_value NUMERIC(18,2) NULL,
    fixed_value NUMERIC(18,2) NULL,
    internal_value NUMERIC(18,2) NULL,
    technical_spec VARCHAR(1000) NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT now(),
    updated_at TIMESTAMP NOT NULL DEFAULT now(),

    CONSTRAINT uk_asset_catalog_items_item_code UNIQUE (item_code),
    CONSTRAINT fk_asset_catalog_items_category
        FOREIGN KEY (category_id) REFERENCES asset.asset_categories(id)
        ON DELETE SET NULL,
    CONSTRAINT ck_asset_catalog_items_catalog_type
        CHECK (catalog_type IN ('ASSET', 'TOOL', 'MATERIAL', 'PRODUCT_REFERENCE')),
    CONSTRAINT ck_asset_catalog_items_values_non_negative
        CHECK (
            (cost_value IS NULL OR cost_value >= 0)
            AND (standard_value IS NULL OR standard_value >= 0)
            AND (fixed_value IS NULL OR fixed_value >= 0)
            AND (internal_value IS NULL OR internal_value >= 0)
        )
);

CREATE INDEX idx_asset_catalog_items_category_id ON asset.asset_catalog_items(category_id);
CREATE INDEX idx_asset_catalog_items_catalog_type ON asset.asset_catalog_items(catalog_type);
CREATE INDEX idx_asset_catalog_items_name ON asset.asset_catalog_items(name);
CREATE INDEX idx_asset_catalog_items_is_active ON asset.asset_catalog_items(is_active);

-- ---------------------------------------------------------------------------
-- Assets
-- ---------------------------------------------------------------------------

CREATE TABLE asset.assets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    asset_code VARCHAR(80) NOT NULL,
    name VARCHAR(255) NOT NULL,
    catalog_item_id BIGINT NULL,
    category_id BIGINT NOT NULL,
    category VARCHAR(80) NULL,
    parent_asset_id BIGINT NULL,
    asset_class VARCHAR(40) NOT NULL,
    fixed_asset_type VARCHAR(40) NULL,
    tool_usage_type VARCHAR(40) NULL,
    serial_number VARCHAR(120) NULL,
    source VARCHAR(120) NULL,
    vendor_id BIGINT NULL,
    assigned_employee_id BIGINT NULL,
    department_id BIGINT NULL,
    site_id BIGINT NULL,
    project_id BIGINT NULL,
    purchase_date DATE NULL,
    use_date DATE NULL,
    depreciation_start_date DATE NULL,
    warranty_until DATE NULL,
    original_cost NUMERIC(18,2) NULL,
    purchase_cost NUMERIC(18,2) NULL,
    accumulated_depreciation NUMERIC(18,2) NOT NULL DEFAULT 0,
    book_value NUMERIC(18,2) NULL,
    residual_value NUMERIC(18,2) NULL,
    depreciation_method VARCHAR(40) NULL DEFAULT 'STRAIGHT_LINE',
    useful_life_months INTEGER NULL,
    useful_life_years INTEGER NULL,
    depreciation_rate NUMERIC(8,4) NULL,
    manufacture_year INTEGER NULL,
    installation_year INTEGER NULL,
    country_code VARCHAR(10) NULL,
    capacity NUMERIC(18,4) NULL,
    capacity_unit VARCHAR(40) NULL,
    real_capacity NUMERIC(18,4) NULL,
    technical_description VARCHAR(2000) NULL,
    status VARCHAR(30) NOT NULL DEFAULT 'IN_STOCK',
    disposal_date DATE NULL,
    disposal_price NUMERIC(18,2) NULL,
    disposal_reason VARCHAR(500) NULL,
    notes VARCHAR(1000) NULL,
    created_at TIMESTAMP NOT NULL DEFAULT now(),
    updated_at TIMESTAMP NOT NULL DEFAULT now(),

    CONSTRAINT uk_assets_asset_code UNIQUE (asset_code),
    CONSTRAINT fk_assets_catalog_item
        FOREIGN KEY (catalog_item_id) REFERENCES asset.asset_catalog_items(id)
        ON DELETE SET NULL,
    CONSTRAINT fk_assets_category
        FOREIGN KEY (category_id) REFERENCES asset.asset_categories(id)
        ON DELETE RESTRICT,
    CONSTRAINT fk_assets_parent_asset
        FOREIGN KEY (parent_asset_id) REFERENCES asset.assets(id)
        ON DELETE SET NULL,
    CONSTRAINT fk_assets_vendor
        FOREIGN KEY (vendor_id) REFERENCES asset.vendors(id)
        ON DELETE SET NULL,
    CONSTRAINT ck_assets_asset_class
        CHECK (asset_class IN ('FIXED_ASSET', 'TOOL_EQUIPMENT')),
    CONSTRAINT ck_assets_fixed_asset_type
        CHECK (fixed_asset_type IS NULL OR fixed_asset_type IN ('TANGIBLE', 'INTANGIBLE')),
    CONSTRAINT ck_assets_tool_usage_type
        CHECK (tool_usage_type IS NULL OR tool_usage_type IN ('SINGLE_USE', 'MULTI_USE')),
    CONSTRAINT ck_assets_fixed_asset_type_required
        CHECK (asset_class <> 'FIXED_ASSET' OR fixed_asset_type IS NOT NULL),
    CONSTRAINT ck_assets_tool_usage_type_required
        CHECK (asset_class <> 'TOOL_EQUIPMENT' OR tool_usage_type IS NOT NULL),
    CONSTRAINT ck_assets_status
        CHECK (status IN ('IN_STOCK', 'ASSIGNED', 'MAINTENANCE', 'LOST', 'DISPOSED')),
    CONSTRAINT ck_assets_depreciation_method
        CHECK (
            depreciation_method IS NULL
            OR depreciation_method IN ('STRAIGHT_LINE', 'DECLINING_BALANCE', 'NONE')
        ),
    CONSTRAINT ck_assets_money_non_negative
        CHECK (
            (original_cost IS NULL OR original_cost >= 0)
            AND (purchase_cost IS NULL OR purchase_cost >= 0)
            AND (accumulated_depreciation >= 0)
            AND (book_value IS NULL OR book_value >= 0)
            AND (residual_value IS NULL OR residual_value >= 0)
            AND (disposal_price IS NULL OR disposal_price >= 0)
        ),
    CONSTRAINT ck_assets_life_non_negative
        CHECK (
            (useful_life_months IS NULL OR useful_life_months >= 0)
            AND (useful_life_years IS NULL OR useful_life_years >= 0)
            AND (depreciation_rate IS NULL OR depreciation_rate >= 0)
        ),
    CONSTRAINT ck_assets_capacity_non_negative
        CHECK (
            (capacity IS NULL OR capacity >= 0)
            AND (real_capacity IS NULL OR real_capacity >= 0)
        )
);

CREATE INDEX idx_assets_asset_code ON asset.assets(asset_code);
CREATE INDEX idx_assets_name ON asset.assets(name);
CREATE INDEX idx_assets_catalog_item_id ON asset.assets(catalog_item_id);
CREATE INDEX idx_assets_category_id ON asset.assets(category_id);
CREATE INDEX idx_assets_category ON asset.assets(category);
CREATE INDEX idx_assets_parent_asset_id ON asset.assets(parent_asset_id);
CREATE INDEX idx_assets_asset_class ON asset.assets(asset_class);
