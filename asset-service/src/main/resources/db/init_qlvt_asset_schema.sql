CREATE TABLE asset.asset_value_snapshots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    asset_id BIGINT NOT NULL,
    snapshot_date DATE NOT NULL,
    original_cost NUMERIC(18,2) NOT NULL,
    period_depreciation NUMERIC(18,2) NOT NULL DEFAULT 0,
    accumulated_depreciation NUMERIC(18,2) NOT NULL DEFAULT 0,
    book_value NUMERIC(18,2) NOT NULL,
    source VARCHAR(80) NOT NULL DEFAULT 'SYSTEM_CALCULATION',
    notes VARCHAR(500) NULL,
    created_at TIMESTAMP NOT NULL DEFAULT now(),

    CONSTRAINT uk_asset_value_snapshots_asset_date_source
        UNIQUE (asset_id, snapshot_date, source),
    CONSTRAINT fk_asset_value_snapshots_asset
        FOREIGN KEY (asset_id) REFERENCES asset.assets(id)
        ON DELETE CASCADE,
    CONSTRAINT ck_asset_value_snapshots_source
        CHECK (source IN ('IMPORT', 'SYSTEM_CALCULATION', 'MANUAL_ADJUSTMENT')),
    CONSTRAINT ck_asset_value_snapshots_money_non_negative
        CHECK (
            original_cost >= 0
            AND period_depreciation >= 0
            AND accumulated_depreciation >= 0
            AND book_value >= 0
        )
);

CREATE INDEX idx_asset_value_snapshots_asset_id ON asset.asset_value_snapshots(asset_id);
CREATE INDEX idx_asset_value_snapshots_snapshot_date ON asset.asset_value_snapshots(snapshot_date);
CREATE INDEX idx_asset_value_snapshots_source ON asset.asset_value_snapshots(source);

