-- QLVT asset schema init script.
--
-- Purpose:
--   Recreate the asset schema from scratch using the improved schema described in:
--   documents/luoc-do-chi-tiet-schema-qlvt.md
--
-- Important:
--   This script is destructive. It drops schema asset and all data inside it.
--   Keep it outside db/migration so Flyway does not run it automatically.
--   Use it only for local/dev database reset or initial schema bootstrap.
--   Production/staging schema changes must go through db/migration/V*.sql.

DROP SCHEMA IF EXISTS asset CASCADE;
CREATE SCHEMA asset;

-- ---------------------------------------------------------------------------
-- Master data: asset categories
-- ---------------------------------------------------------------------------

CREATE TABLE asset.asset_categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(60) NOT NULL,
    name VARCHAR(180) NOT NULL,
    parent_id BIGINT NULL,
    asset_class VARCHAR(40) NOT NULL,
    description VARCHAR(500) NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT now(),
    updated_at TIMESTAMP NOT NULL DEFAULT now(),

    CONSTRAINT uk_asset_categories_code UNIQUE (code),
    CONSTRAINT fk_asset_categories_parent
        FOREIGN KEY (parent_id) REFERENCES asset.asset_categories(id)
        ON DELETE RESTRICT,
    CONSTRAINT ck_asset_categories_asset_class
        CHECK (asset_class IN ('FIXED_ASSET', 'TOOL_EQUIPMENT'))
);

CREATE INDEX idx_asset_categories_parent_id ON asset.asset_categories(parent_id);
CREATE INDEX idx_asset_categories_asset_class ON asset.asset_categories(asset_class);
CREATE INDEX idx_asset_categories_is_active ON asset.asset_categories(is_active);

